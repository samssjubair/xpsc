#define ll long long int

using namespace std;

int main()
{
    ios_base::sync_with_stdio(false);
    cin.tie(0);
    cout.tie(0);

    int t;cin>>t;
    while(t--){
        int n;cin>>n;
        int a[n];
        for(int i=0;i<n;i++) cin>>a[i]; 

        sort(a,a+n);
        ll sum=0;
        for(int i=0;i<n;i++){
            if(i<n/2) sum+=a[i];
            else sum-=a[i];
            
        }
        cout<<sum<<endl;
    }
    return 0;
}